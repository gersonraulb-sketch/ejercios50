base = int(input("Ingrese la base del rectángulo: ")) # Pedir al usuario la base del rectángulo
altura = int(input("Ingrese la altura del rectángulo: ")) # Pedir al usuario la altura del rectángulo

area = base * altura

print(f"El área del rectángulo con base de {base} y altura de {altura} es: {area}") # Mostrar el área del rectángulo