grados_celcius = int(input("Ingrese la temperatura en grados Celsius: ")) # Pedir la temperatura en grados Celsius
grados_fahrenheit = (grados_celcius * 9/5) + 32 # Convertir a grados Fahrenheit

print(f"los {grados_celcius} que ingreso son: {grados_fahrenheit} grados fahrenheit") # Mostrar la temperatura en grados Fahrenheit