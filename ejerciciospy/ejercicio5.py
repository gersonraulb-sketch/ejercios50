n1 = int(input("Ingrese la primer nota: ")) # Pedir el primer numero
n2 = int(input("Ingrese la segunda nota: ")) # Pedir el segundo numero
n3 = int(input("Ingrese la tercer nota: ")) # Pedir el tercer numero

suma = n1 + n2 + n3 # Sumar los numeros
promedio = suma / 3 # Calcular el promedio

print(f"El promedio de las notas es: {promedio}") # Mostrar el promedio